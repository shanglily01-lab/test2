-- ======================================================================
-- 设置 MySQL 时区为 UTC+8
-- ======================================================================

-- 1. 查看当前时区设置
SELECT
    @@global.time_zone AS '全局时区',
    @@session.time_zone AS '会话时区',
    NOW() AS '当前时间',
    UTC_TIMESTAMP() AS 'UTC时间';

-- 2. 设置全局时区为 +08:00 (UTC+8)
SET GLOBAL time_zone = '+08:00';

-- 3. 设置当前会话时区为 +08:00
SET time_zone = '+08:00';

-- 4. 验证时区设置
SELECT
    @@global.time_zone AS '全局时区(应为+08:00)',
    @@session.time_zone AS '会话时区(应为+08:00)',
    NOW() AS '当前时间(UTC+8)',
    UTC_TIMESTAMP() AS 'UTC时间';

-- ======================================================================
-- 注意事项:
-- 1. 全局时区设置会在 MySQL 重启后失效
-- 2. 如需永久生效，需要在 my.cnf 中添加:
--    [mysqld]
--    default-time-zone='+08:00'
-- 3. 应用程序需要重新连接数据库才能使用新时区
-- ======================================================================
